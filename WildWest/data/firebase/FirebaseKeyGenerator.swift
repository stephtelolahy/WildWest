import Firebase

class FirebaseKeyGenerator: KeyGeneratorProtocol {
    
    func autoId() -> String {
        guard let key = Database.database().reference().childByAutoId().key else {
            fatalError("Cannot get autogenerated unique identifier")
        }
        
        return key
    }
}
